import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const args = new Map();
const flags = new Set();

for (let index = 2; index < process.argv.length; index += 1) {
  const arg = process.argv[index];
  if (arg === '--check') {
    flags.add('check');
  } else if (arg.startsWith('--')) {
    args.set(arg.slice(2), process.argv[index + 1]);
    index += 1;
  }
}

const input = args.get('input');
const output = args.get('output');
const checkOnly = flags.has('check');

// Safety invariant: needs_review records are never promoted into overlays.

if (!input || !output) {
  console.error('Usage: node scripts/promote-timetable-candidates.mjs --input <candidate.json> --output <overlay.json> [--check]');
  process.exit(1);
}

function readJson(relativePath) {
  return JSON.parse(readFileSync(path.join(root, relativePath), 'utf8'));
}

function toOverlayRecord(record) {
  return {
    country_id: record.country_id,
    racecourse_id: record.racecourse_id,
    racecourse_name: record.racecourse_name,
    date: record.date,
    start_time_local: record.start_time_local,
    timezone: record.timezone,
    racing_type: record.racing_type,
    source_id: record.source_id,
    source_url: record.source_url,
    last_checked_at: record.source_checked_at,
    status: 'source-reviewed',
    confidence: record.confidence,
    notes: record.notes
  };
}

function assertSafeRecord(record, seen) {
  const requiredFields = [
    'candidate_id',
    'country_id',
    'racing_system_id',
    'racecourse_id',
    'racecourse_name',
    'date',
    'start_time_local',
    'timezone',
    'racing_type',
    'source_id',
    'source_url',
    'source_checked_at',
    'extraction_method',
    'status',
    'confidence',
    'review_status',
    'notes'
  ];

  for (const field of requiredFields) {
    if (!(field in record)) throw new Error(`${record.candidate_id ?? 'unknown'} missing ${field}`);
  }

  if (record.review_status !== 'approved') throw new Error(`${record.candidate_id} is not approved`);
  if (record.status !== 'candidate' && record.status !== 'source-reviewed') {
    throw new Error(`${record.candidate_id} has unsupported status ${record.status}`);
  }

  const key = `${record.date}:${record.racecourse_id}:${record.source_id}`;
  if (seen.has(key)) throw new Error(`duplicate approved candidate key: ${key}`);
  seen.add(key);

  const serialized = JSON.stringify(record).toLowerCase();
  for (const forbidden of ['raw html', 'source body', 'racecard', 'entries', 'horse names', 'jockey names', 'trainer names', 'odds', 'results', 'payouts', 'prediction', 'tips', 'betting advice']) {
    if (serialized.includes(forbidden)) throw new Error(`${record.candidate_id} contains forbidden marker: ${forbidden}`);
  }
}

function buildOverlay(candidateFile) {
  if (candidateFile.schema_version !== 'timetable-candidates-v0') {
    throw new Error('candidate file must use timetable-candidates-v0');
  }

  const approvedRecords = (candidateFile.records ?? []).filter((record) => record.review_status === 'approved');
  const seen = new Set();

  for (const record of approvedRecords) {
    assertSafeRecord(record, seen);
  }

  const overlayRecords = approvedRecords
    .map(toOverlayRecord)
    .sort((a, b) => `${a.date}:${a.racecourse_id}:${a.source_id}`.localeCompare(`${b.date}:${b.racecourse_id}:${b.source_id}`));

  return {
    schema_version: 'timetable-overlay-promoted-v0',
    generated_at: candidateFile.generated_at,
    promoted_from: input,
    source_adapter_id: candidateFile.source_adapter_id,
    country_id: candidateFile.country_id,
    candidate_window: candidateFile.candidate_window,
    records: overlayRecords,
    sources: [...new Set(overlayRecords.map((record) => record.source_id))].sort(),
    notes: [
      'Generated by scripts/promote-timetable-candidates.mjs from approved candidate records only.',
      'Needs-review and rejected candidate records are excluded.',
      'No raw source body, racecard, odds, results, payouts, predictions, or tips are stored.'
    ]
  };
}

try {
  const candidateFile = readJson(input);
  const overlay = buildOverlay(candidateFile);
  const serialized = `${JSON.stringify(overlay, null, 2)}\n`;
  const outputPath = path.join(root, output);

  if (checkOnly) {
    if (!existsSync(outputPath)) throw new Error(`missing output: ${output}`);
    const current = readFileSync(outputPath, 'utf8');
    if (current !== serialized) throw new Error(`promotion output is stale: ${output}`);
    console.log(`Promotion output is up to date: ${output}`);
  } else {
    mkdirSync(path.dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, serialized);
    console.log(`Wrote ${output} with ${overlay.records.length} promoted records.`);
  }
} catch (error) {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
}
